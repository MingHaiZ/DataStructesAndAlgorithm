class Solution {
    public String longestPalindrome(String s) {
        int maxLen = 0;
        int maxStart = 0;
        int left, right;
        for (int i = 0; i < s.length(); i++) {
//            bab
            left = i;
            right = i;
            while (left >= 0 && right < s.length() && s.charAt(left) == s.charAt(right)) {
                if (right - left + 1 > maxLen) {
                    maxLen = right - left + 1;
                    maxStart = left;
                }
                left--;
                right++;
            }

//            baab
            left = i;
            right = i + 1;
            while (left >= 0 && right < s.length() && s.charAt(left) == s.charAt(right)) {
                if (right - left + 1 > maxLen) {
                    maxLen = right - left + 1;
                    maxStart = left;
                }
                left--;
                right++;
            }
        }
        return s.substring(maxStart, maxStart + maxLen);
    }
}
