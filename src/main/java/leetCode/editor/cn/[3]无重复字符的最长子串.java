class Solution {
    public int lengthOfLongestSubstring(String s) {
        int slow = 0, max = 0;
        Set<Character> set = new HashSet<>();
        for (int i = 0; i < s.length(); i++) {
            while (set.contains(s.charAt(i))) {
                set.remove(s.charAt(slow));
                slow++;
            }
            set.add(s.charAt(i));
            max = Math.max(max, i - slow + 1);
        }
        return max;
    }
}
